#include "libft.h"

int	hex_upper(unsigned int n)
{
	int	sum;

	sum = 1;
	if (n / 16 != 0 && n > 0)
	{
		sum += hex_upper(n / 16);
	}
	write (1, &"0123456789ABCDEF"[n % 16], 1);
	return (sum);
}
