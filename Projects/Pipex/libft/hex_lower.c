#include "libft.h"

int	hex_lower(unsigned int n)
{
	int	sum;

	sum = 1;
	if (n / 16 != 0 && n > 0)
	{
		sum += hex_lower(n / 16);
	}
	write (1, &"0123456789abcdef"[n % 16], 1);
	return (sum);
}
