#include "libftprintf.h"

int	ft_putnumprintf(int n)
{
	int	i;

	i = 0;
	if (n == 0)
		i = 1;
	else if (n < 0)
	{
		if (n == -2147483648)
			i = 11;
		else
			n = -n;
	}
	while (n > 0)
	{
		n /= 10;
		i++;
	}
	return (i);
}
